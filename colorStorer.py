class ColorStorer:

    @staticmethod
    def get_white() -> str:
        return '#FFFFFF'

    @staticmethod
    def get_yellow() -> str:
        return '#FFED01'

    @staticmethod
    def get_red() -> str:
        return '#FF2E2E'

    @staticmethod
    def get_orange() -> str:
        return '#FF6600'

    @staticmethod
    def get_blue() -> str:
        return '#006EE6'

    @staticmethod
    def get_green() -> str:
        return '#08f26E'